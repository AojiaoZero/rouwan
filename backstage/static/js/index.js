var $i={
	r:function(){
		setTimeout(function(){location.reload();},2000)
	},
	exitH:0,
	exitCount:5,
	exit:function(){
		if($i.exitCount<0){
			$("#exh").html('Exiting...');
			$m.post('./island?sp=1',{"do":"exit"});
			$i.exitCount=5;
			return;
		}
		$("#rsa").hide();
		$("#exa").hide();
		$("#exh").html('Exiting in '+$i.exitCount+'s ... <a href="javascripg:void(0)" onclick="$i.cexit()">Cancel</a>');
		$i.exitCount--;
		$i.exitH=setTimeout($i.exit,1000);
	},
	cexit:function(){
		clearTimeout($i.exitH);
		$i.exitCount=5;
		$("#rsa").show();
		$("#exa").show();
		$("#exh").html('');
	},
	restartH:0,
	restartCount:5,
	restart:function(){
		if($i.restartCount<0){
			$("#rsh").html('Restarting...');
			$m.post('./island?sp=1',{"do":"restart"});
			$i.restartCount=5;
			return;
		}
		$("#rsa").hide();
		$("#exa").hide();
		$("#rsh").html('Restarting in '+$i.restartCount+'s ... <a href="javascripg:void(0)" onclick="$i.crestart()">Cancel</a>');
		$i.restartCount--;
		$i.restartH=setTimeout($i.restart,1000);
	},
	crestart:function(){
		clearTimeout($i.restartH);
		$i.restartCount=5;
		$("#rsa").show();
		$("#exa").show();
		$("#rsh").html('');
	},
	oid:function(){
		$m.dbu("#oidBu","Loading...");
		$m.post('./island?sp=1',$m.bind('bindOid'));
	},
	oided:function(re,oid){
		$m.bindFind("bindOids","oid").val(oid);
		re=JSON.stringify(re,null,'	');
		editor.setValue(re);
		editor.clearSelection();
		$m.ebu("#oidBu","Edit");
		$("#objectEditor").show();
		$("#objectBar").show();
	},
	oidsc:function(){
		$("#objectEditor").hide();
		$("#objectBar").hide();
	},
	oidd:function(){
		$m.dbu("#oidBu","Edit");
		$m.dbu("#oidsBu","Save");
		$m.dbu("#oiddBu","Loading...");
		$m.dbu("#oidscBu","Cancel");
	}
};